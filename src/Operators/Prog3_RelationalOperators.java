package Operators;

public class Prog3_RelationalOperators {
    public static void main(String[] args) {
        //Relational/comparision Operators
        System.out.println("Relational/comparision Operators");
        int a = 60, b = 30;
        System.out.println("(a>b) : " + (a>b));
        System.out.println("(a>=b) : " + (a>=b));
        System.out.println("(a<b) : " + (a<b));
        System.out.println("(a<=b) : " + (a<=b));
        System.out.println("(a==b) : " + (a==b));
        System.out.println("(a!=b) : " + (a!=b));
    }
}
